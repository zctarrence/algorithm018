class Solution {
    public boolean isAnagram(String s, String t) {
        int ans = 0;
        int a = 0;
        int b = 0;
        int e = 0;
        int f  = 0;
        for(char c : s.toCharArray()){
            ans ^= c;
            a += c;
            e |= c;
        }
        for(char c : t.toCharArray()){
            ans ^= c;
            b += c;
            f |= c;
        }
        return ans == 0 && a == b && e == f;
    }
}
